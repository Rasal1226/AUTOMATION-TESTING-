package december22;

public class Forloop7 {

	public void method1() {
		for(int i = 1;i<=10;i++) {
			for(int j=10;j<=10;j++) {
				System.out.print("* ");
		
			}
			System.out.println();
		}
		
	}
	public static void main(String [] args) {
		Forloop7 object = new Forloop7();
		object.method1();
	}
}
