package december13;

public class Byusingconstructor {

	int a;
	int b; 
	public Byusingconstructor(int a, int b) {
		this.a = a;
		this.b = b;
	}
	
	public void method1() {
	
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(a--);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a--);
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(--a);
		System.out.println(+a);
		System.out.println(-a);
		
		
		System.out.println(b);
		System.out.println(b++);
		System.out.println(b++);
		System.out.println(b--);
		System.out.println(++b);
		System.out.println(b);
		System.out.println(--b);
		System.out.println(b--);
		System.out.println(b++);
		System.out.println(b++);
		System.out.println(++b);
		System.out.println(b--);
		System.out.println(++b);
		System.out.println(b);
		System.out.println(--b);
		System.out.println(+b);
		System.out.println(-b);
		
	}
}
